import { Injectable } from '@angular/core';

@Injectable({
  providedIn: 'root'
})
export class ImgService {

  pictures = [
    { name: "The Drunkenness of Noah", src: "https://upload.wikimedia.org/wikipedia/commons/thumb/0/07/Ivresse_de_noe.jpg/300px-Ivresse_de_noe.jpg" },
    { name: "The Deluge", src: "https://upload.wikimedia.org/wikipedia/commons/thumb/d/d6/Michelangelo%2C_Deluge_02.jpg/264px-Michelangelo%2C_Deluge_02.jpg" },
    { name: "The First Day of Creation", src: "https://upload.wikimedia.org/wikipedia/commons/thumb/a/ab/First_Day_of_Creation.jpg/245px-First_Day_of_Creation.jpg" },
    { name: "The Libyan Sibyl", src: "https://upload.wikimedia.org/wikipedia/commons/thumb/5/5c/Michelangelo_the_libyan.jpg/288px-Michelangelo_the_libyan.jpg" },
    { name: "Ignudo", src: "https://upload.wikimedia.org/wikipedia/commons/thumb/c/c9/%27Ignudo%27_by_Michelangelo_JBU33.jpg/225px-%27Ignudo%27_by_Michelangelo_JBU33.jpg" },
    { name: "The Prophet Jeremiah", src: "https://upload.wikimedia.org/wikipedia/commons/thumb/9/9b/Michelangelo_Buonarroti_027.jpg/218px-Michelangelo_Buonarroti_027.jpg" }
  ]

  constructor() { }

  getPictures() {
    return this.pictures;
  }

  getSrc(name?: string) {
    return this.pictures.find(picture => picture.name == name)?.src;
  }
}
